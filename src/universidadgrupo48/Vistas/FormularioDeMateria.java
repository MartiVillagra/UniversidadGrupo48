    private void jbEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbEliminarActionPerformed
      int codigo= Integer.parseInt(jtCodigo.getText());
      MateriaData matedata = new MateriaData();
     if(matedata.buscarMateriaPorID(codigo)!=null){
         matedata.eliminarMateria(codigo);
     }
    
     
    }//GEN-LAST:event_jbEliminarActionPerformed

    private void jbGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbGuardarActionPerformed
     
        String nombre= jtNombreMateria.getText();
       int anio=Integer.parseInt(jtAnio.getText());
       
       boolean estado = jrbEstado.isSelected();
       
       Materia mate =new Materia(nombre,anio,estado);
       MateriaData mateData = new MateriaData();
       mateData.guardarMateria(mate);

      
       
    }//GEN-LAST:event_jbGuardarActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    int codigo=Integer.parseInt(jtCodigo.getText());
    MateriaData mateData =new MateriaData();
    jtNombreMateria.setText(mateData.buscarMateriaPorID(codigo).getNombre());
    jtAnio.setText(mateData.buscarMateriaPorID(codigo).getAnioMateria()+"");
    jrbEstado.setSelected(mateData.buscarMateriaPorID(codigo).isActivo());
    
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jbModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbModificarActionPerformed
        int codigo =Integer.parseInt( jtCodigo.getText());
        String nombre =jtNombreMateria.getText();
        int anio =Integer.parseInt(jtAnio.getText());
        boolean estado =jrbEstado.isSelected();
        Materia mate =new Materia(codigo,nombre,anio,estado);
        MateriaData mateData =new MateriaData();
        mateData.modificarMateria(mate);
    }//GEN-LAST:event_jbModificarActionPerformed


